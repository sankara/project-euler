(defn max-sum-4 [grid]
    (reduce max (for [i (range 20)]
        (reduce max (for [j (range 20)]
            (max (if (< i 16) (reduce * (for [k (range 4)] (aget grid (+ i k) j))) 0)
                (if (< j 16) (reduce * (for [k (range 4)] (aget grid i (+ j k)))) 0)
                (if (and (< i 16) (> j 3)) (reduce * (for [k (range 4)] (aget grid (+ i k) (- j k)))) 0)
                (if (and (< i 16) (< j 16)) (reduce * (for [k (range 4)] (aget grid (+ i k) (+ j k)))) 0)
